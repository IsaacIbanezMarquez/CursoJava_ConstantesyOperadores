public class Declaraciones_Operadores
{
    public static void main(String[] args)
    {
        // Iniciar variable con valor.
        int a = 5;

        // Iniciar variable sin valor.
        int b;

        // Instanciar valor de variable declarada:
        b = 7;

        // Variable con valor de la suma de otras variables.
        int c = b + a;

        // Variable++ , incrementa en 1 al valor de la variable.
        c++;

        // Variable+= , incrtementa en X el valor de la variable:

        c+=6;
        c-=6;

        // Comportamiento IntelliSense, System.out.println()[ imprimir en consola]:

        System.out.println(c);

    }
}
